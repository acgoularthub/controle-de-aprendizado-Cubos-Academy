// métodos de array com funções de callback

/*

every() - retorna true se todos os elementos do array satisfazem a condição
some() - retorna true se pelo menos um elemento do array satisfaz a condição
find() - retorna o primeiro elemento do array que satisfaz a condição
findIndex() - retorna o índice do primeiro elemento do array que satisfaz a condição
filter() - retorna um novo array com todos os elementos que satisfazem a condição
map() - retorna um novo array com a transformação de todos os elementos de acordo com a função de callback

*/