let x = 10;
x++; // x = x + 1
console.log(x); // 11

x--; // x = x - 1
console.log(x); // 10

++x; // x = x + 1
console.log(x); // 11

--x; // x = x - 1
console.log(x); // 10

// a diferença de ++x e x++ é que o primeiro incrementa antes de usar o valor e o 
//segundo incrementa depois de usar o valor